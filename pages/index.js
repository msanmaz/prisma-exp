export async function getServerSideProps(context) {

  const session = await getSession(context);

  let cars = await prisma.Tweet.findMany({
    orderBy:{
      createdAt:'desc'
    }
  })
  cars = JSON.parse(JSON.stringify(cars))


  return {
    props: {session,cars }
  };
}

